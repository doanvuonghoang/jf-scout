package com.jf.commons.datamodels.hrm.employee;

import java.io.Serializable;
import java.util.Date;

import com.j256.ormlite.field.DatabaseField;
import com.j256.ormlite.table.DatabaseTable;
import com.jf.commons.datamodels.RecordHistEntity;

/**
 *
 * @author Hoàng Doãn
 */
@DatabaseTable(tableName = "hrm_Educations")
public class Education extends RecordHistEntity implements Serializable {
	private static final long serialVersionUID = 1L;
	
	@DatabaseField(foreign = true, canBeNull = false)
	private Employee employee;
	
	@DatabaseField(canBeNull = false)
	private Date fromDate;
	
	@DatabaseField
	private Date toDate;
	
	@DatabaseField(canBeNull = false)
	private String trainingUnit;
	
	@DatabaseField(canBeNull = false)
	private String address;
	
	@DatabaseField(canBeNull = false)
	private String title;
	
	@DatabaseField(canBeNull = false)
	private String degree;
	
	/**
	 * @return the employee
	 */
	public Employee getEmployee() {
		return employee;
	}

	/**
	 * @param employee the employee to set
	 */
	public void setEmployee(Employee employee) {
		this.employee = employee;
	}

	/**
	 * @return the fromDate
	 */
	public Date getFromDate() {
		return fromDate;
	}

	/**
	 * @param fromDate the fromDate to set
	 */
	public void setFromDate(Date fromDate) {
		this.fromDate = fromDate;
	}

	/**
	 * @return the toDate
	 */
	public Date getToDate() {
		return toDate;
	}

	/**
	 * @param toDate the toDate to set
	 */
	public void setToDate(Date toDate) {
		this.toDate = toDate;
	}

	/**
	 * @return the trainingUnit
	 */
	public String getTrainingUnit() {
		return trainingUnit;
	}

	/**
	 * @param trainingUnit the trainingUnit to set
	 */
	public void setTrainingUnit(String trainingUnit) {
		this.trainingUnit = trainingUnit;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}

	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * @return the degree
	 */
	public String getDegree() {
		return degree;
	}

	/**
	 * @param degree the degree to set
	 */
	public void setDegree(String degree) {
		this.degree = degree;
	}
}
